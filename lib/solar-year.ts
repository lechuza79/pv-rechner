//
// Woher: PVGIS (EU JRC), Stundenreihen fuer die Mitte Deutschlands (51,3° N / 9,5° O),
// 1 kWp, 14 % Systemverluste. Aus 2014–2023 wurde das TYPISCHE Jahr gewaehlt: 2023
// traf mit 1.012,7 kWh/kWp das Zehnjahresmittel (1.012,0) auf 0,07 % genau.
//
// Warum ein echtes Jahr und NICHT der Mittelwert mehrerer Jahre: Mitteln glaettet
// genau das weg, worauf es ankommt — die Sonnentage. Ein Wechselrichter kappt nur an
// Spitzen, ein Speicher fuellt sich nur bei Ueberschuss. Ein "Durchschnittstag" hat
// weder das eine noch das andere.
//
// Warum je Ausrichtung eine EIGENE Reihe (statt eines Faktors auf die Suedreihe):
// Ost/West hat zwei flache Spitzen statt einer hohen — es wird also ganz anders
// gekappt (10,3 % statt 20,4 % bei 2 kWp an 800 W) und laedt den Speicher anders.
// Ein Faktor kann das prinzipiell nicht abbilden, er skaliert nur die Menge.
// Nebenbei hat der Wechsel auf echte Reihen aufgedeckt, dass die frueheren Faktoren
// grob daneben lagen (Ost/West 0,85 statt real 0,51; Nord 0,5 statt real 0,20).
//
// Jahresertraege der Reihen (kWh/kWp): Sued aufgestaendert 1013,3 · Sued senkrecht
// 694,2 (0,69) · Ost/West senkrecht 513,6 (0,51) · Nord senkrecht 198,2 (0,20).
//
// Verdichtung: Je Monat sechs Tagestypen (nach Tagesertrag in Sextile geteilt) mit
// ihrer tatsaechlichen Haeufigkeit. Gemittelt wird nur INNERHALB eines Typs — sonnige
// Tage also nur mit sonnigen, dadurch bleibt die Mittagsspitze stehen.
// GEMESSENE Abweichung gegen die echten 8.760 Stunden im haertesten Clipping-Fall
// (2 kWp an 800 W, Sued aufgestaendert): +3,5 % zu optimistisch; ohne Clipping null.
//
// Nutzung: Die Reihe liefert die VERTEILUNG und die Ausrichtung. Die MENGE am
// Standort kommt aus den PVGIS-Monatswerten (/api/pvgis), auf die jeder Monat
// skaliert wird. Lizenz: PVGIS/EU JRC, frei mit Quellenangabe (lib/data-sources.ts).

