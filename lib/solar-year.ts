/** Monatsertrag der Referenzreihe in kWh/kWp (alle Tagestypen × ihre Haeufigkeit). */
export function referenceMonthKwh(month: number): number {
  return SOLAR_YEAR_DE[month].reduce((s, t) => s + (t.days * t.w.reduce((a, b) => a + b, 0)) / 1000, 0);
}

/** Jahresertrag der Referenzreihe in kWh/kWp. */
export const REFERENCE_YEAR_KWH = Array.from({ length: 12 }, (_, m) => referenceMonthKwh(m)).reduce((a, b) => a + b, 0);
