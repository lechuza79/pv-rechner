// Typisches Sonnenjahr Deutschland — GETEILTE Rechen-Basis (siehe CLAUDE.md).
//
// Woher: PVGIS (EU JRC), Stundenreihe fuer die Mitte Deutschlands (51,3° N / 9,5° O),
// 1 kWp, optimale Neigung, 14 % Systemverluste. Aus 2014–2023 wurde das TYPISCHE
// Jahr gewaehlt: 2023 traf mit 1.012,7 kWh/kWp das Zehnjahresmittel (1.012,0) auf
// 0,07 % genau.
//
// Warum ein echtes Jahr und NICHT der Mittelwert mehrerer Jahre: Mitteln glaettet
// genau das weg, worauf es ankommt — die Sonnentage. Ein Wechselrichter kappt nur
// an Spitzen, ein Speicher fuellt sich nur bei Ueberschuss. Ein "Durchschnittstag"
// hat weder das eine noch das andere.
//
// Verdichtung: Je Monat sechs Tagestypen (nach Tagesertrag in Sextile geteilt) mit
// ihrer tatsaechlichen Haeufigkeit. Gemittelt wird nur INNERHALB eines Typs —
// sonnige Tage also nur mit sonnigen, dadurch bleibt die Mittagsspitze stehen.
//
// GEMESSENE Abweichung gegen die echten 8.760 Stunden (2 kWp an 800 W, also der
// harteste Clipping-Fall): echt 1.609 kWh / 20,5 % gekappt, hier 1.665 kWh /
// 17,8 % gekappt → +3,5 % zu optimistisch. Bei Anlagen ohne Clipping ist die
// Abweichung null (die Energie stimmt exakt). Bewusster Kompromiss fuer eine
// kleine, statische Datei ohne Endpunkt/Cache. Zum Vergleich: 3 Typen waeren
// +5,2 %, 12 Typen +2,4 % bei doppelter Datenmenge.
//
// Nutzung: Die Reihe liefert die VERTEILUNG. Die MENGE kommt pro Standort aus den
// PVGIS-Monatswerten (/api/pvgis), auf die jeder Monat skaliert wird — ein
// sonnigerer Standort bekommt proportional hoehere Spitzen und damit mehr
// Clipping. Lizenz: PVGIS/EU JRC, frei mit Quellenangabe (lib/data-sources.ts).

